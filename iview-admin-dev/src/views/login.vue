<style lang="less">
    @import './login.less';
</style>

<template>
    <div class="login" @keydown.enter="handleSubmit">
        <div class="login-con">
            <Card :bordered="false">
                <p slot="title">
                    <Icon type="log-in"></Icon>
                    欢迎登录
                </p>
                <div class="form-con">
                    <Form ref="loginForm" :model="loginForm" :rules="loginRules">
                        <FormItem prop="admin_id">
                            <Input v-model="loginForm.admin_id" placeholder="请输入用户名">
                            <span slot="prepend">
                                    <Icon :size="16" type="person"></Icon>
                                </span>
                            </Input>
                        </FormItem>
                        <FormItem prop="password">
                            <Input type="password" v-model="loginForm.password" placeholder="请输入密码">
                            <span slot="prepend">
                                    <Icon :size="14" type="locked"></Icon>
                                </span>
                            </Input>
                        </FormItem>
                        <FormItem>
                            <Button @click="handleSubmit()" type="primary" long>登录</Button>
                        </FormItem>
                    </Form>
                    <p class="login-tip">输入任意用户名和密码即可</p>
                </div>
            </Card>
        </div>
    </div>
</template>

<script>
    import Cookies from 'js-cookie';
    import { doLogin } from '../libs/vueHelper'
    export default {
        name:"login",
        data () {
            return {
                loginForm: {
                    admin_id: 'adminzwq',
                    password: '123456'
                },
                loginRules: {
                    admin_id: [
                        {required: true, message: '账号不能为空', trigger: 'blur'},
                        {max: 16, message: '账号应小于16个字符', trigger: 'blur'}
                    ],
                    password: [
                        {required: true, message: '密码不能为空', trigger: 'blur'},
                        {min: 6, max: 16, message: '密码长度应为6-16位', trigger: 'blur'}
                    ]
                }
            };
        },
        methods: {
            handleSubmit (){
                this.$refs.loginForm.validate((valid) => {
                    if (valid) {
                        let data = {
                            'usr': this.loginForm.admin_id,
                            'pwd': this.loginForm.password
                        }
                        doLogin(this, data)
                    } else {
                        return false
                    }
                });
            }
        }
    };
</script>

